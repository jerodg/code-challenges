/// Doubles the input integer.
///
/// This function takes an integer as an argument and returns the result of multiplying the integer by 2.
///
/// Example:
/// ```dart
/// var result = doubleInteger(2);
/// print(result);  // Outputs: 4
/// ```
///
/// [i]: The integer to double.
/// Returns the result of the multiplication operation.
int doubleInteger(int i) {
  // Multiplying the integer by 2.
  return i * 2;
}
